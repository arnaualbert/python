#######################################################################
# Infinite recursion
#######################################################################

#----------------------------------------------------------------------
def say_hello_v1():
    
    while True:
        print("Hello!")

#----------------------------------------------------------------------
def say_hello_v2():
    
    print("Hello!")
    say_hello_v2()


#######################################################################
# Limited recursion
#######################################################################

#----------------------------------------------------------------------
def say_hello_v3():
    
    for _ in range(5):
        print("Hello!")

#----------------------------------------------------------------------
def say_hello_v4(num: int):

    # Recursive branch
    if (num > 0):
        print("Hello!")
        say_hello_v4(num-1)

    # Termination branch
    if (num == 0):
        print("Finished!")

#----------------------------------------------------------------------
def say_hello_v5(num: int):

    # Recursive branch
    if (num > 0):
        print("Hello!")
        say_hello_v5(num-1)

    # Termination branch
    elif (num <= 0):
        pass



